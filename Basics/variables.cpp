#include <iostream>
using namespace std;

int main() {
    int x; // Declaration
    x = 5; // Initialization
    cout << "x: " << x << endl;

    // Declaring variables
    int age = 20;           // Integer variable
    float weight = 65.5;    // Floating-point variable
    char grade = 'A';       // Character variable
    bool passed = true;     // Boolean variable

    // Output the values of variables
    cout << "Age: " << age << endl;
    cout << "Weight: " << weight << endl;
    cout << "Grade: " << grade << endl;
    cout << "Passed: " << passed << endl;

    // Updating the value of the variables
    age = 23;
    cout << "Updated Age: " << age << endl;



    return 0;
}
